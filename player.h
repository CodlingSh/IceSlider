class Player {
  private:
    Arduboy2 *ab;
    uint8_t *spr = ship;
    int8_t x = 10;
    int8_t y = 28;
    int16_t subY = 448;
    int8_t vel = 0;
    int8_t maxVel = 16;
    int8_t acc = 0;
    Bullet bullet;
    bool bulletActive = false;
    bool dying = false;
    int8_t deathTimer = 0;
    bool moving = true;
    
  public:
    Player(Arduboy2 *ab_ptr) : ab(ab_ptr), bullet(ab_ptr) {}

    Bullet& getBullet() {
      return bullet;
    }

    uint8_t getX() {
      return x;
    }

    uint8_t getY() {
      return y;
    }

    bool isDying() {
      return dying;
    }

    void die() {
      dying = true;
      moving = false;
      spr = shipExp;        
    }

    void fire() {
      if (!bullet.getActive()) {
        bullet.setX(x + 5);
        bullet.setY(y + 3);
        bullet.activate();
      }
    }

    void update() {
      bullet.update();

      if (dying) {
        deathTimer++;

        if (deathTimer >= 20) {
          x = 222;
          y = 222;
        }

        return;
      }

      if (!moving) {
        return;
      }

      // Movement
      if (ab->pressed(UP_BUTTON)) {
        acc = -1;
      }
      if (ab->notPressed(UP_BUTTON) && ab->notPressed(DOWN_BUTTON)) {
        acc = 0;
        vel = 0;
      }
      
      
      if (ab->pressed(DOWN_BUTTON)) {
        acc = 1;
      } 

      vel += acc;

      if (vel > maxVel) {
        subY += maxVel;
      } else if (vel < (maxVel * -1)) {
        subY += (maxVel * -1);
      } else {
        subY += vel;
      }

      y = subY / 16;

      

      // Shooting
      if (ab->pressed(A_BUTTON)) {
        fire();
      }
    }

    void draw() {
      if (dying) {
        Sprites::drawPlusMask(x, y, spr, deathTimer / 4);
      } else {
        Sprites::drawPlusMask(x, y, spr, 0);
      }

      
      bullet.draw();
    }
};

