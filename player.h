class Player {
  private:
    Arduboy2 *ab;
    const uint8_t *spr = ship;
    int8_t x = 10;
    int8_t y = 28;
    uint8_t frame = 0;
    Bullet bullet;
    bool bulletActive = false;
    bool dying = false;
    int8_t deathTimer = 0;
    bool moving = true;
    
  public:
    Player(Arduboy2 *ab_ptr) : ab(ab_ptr), bullet(ab_ptr) {}

    Bullet& getBullet() {
      return bullet;
    }

    uint8_t getX() {
      return x;
    }

    uint8_t getY() {
      return y;
    }

    void die() {
      dying = true;
      moving = false;
      spr = shipExp;        
    }

    void fire() {
      if (!bullet.getActive()) {
        bullet.setX(x + 5);
        bullet.setY(y + 3);
        bullet.activate();
      }
    }

    void update() {
      bullet.update();

      if (!moving) {
        return;
      }

      // Movement
      if (ab->pressed(UP_BUTTON)) {
        y--;
      }
      else if (ab->pressed(DOWN_BUTTON)) {
        y++;
      }

      // Shooting
      if (ab->pressed(A_BUTTON)) {
        fire();
      }

      if (ab->pressed(B_BUTTON)) {
        die();
      }

      // Explosiong
      if (dying) {
        deathTimer++;

        if (deathTimer >= 10) {
          dying = false;
          deathTimer = 0;
        }
      }
    }

    void draw() {
      if (dying) {
        Sprites::drawPlusMask(x, y, spr, deathTimer / 2);
      } else {
        Sprites::drawPlusMask(x, y, spr, frame);
      }

      
      bullet.draw();
    }
};

